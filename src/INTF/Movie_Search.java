    private void search() {
        try {
            DefaultTableModel k = (DefaultTableModel) jTable1.getModel();
            k.setRowCount(0);
            ResultSet rs = ovcconn.DB().createStatement().executeQuery("SELECT * FROM `ovc`.`movie_lib` ;");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(3));
                v.add(rs.getString(5));
                v.add(rs.getString(4));
                v.add(rs.getString(7));
                v.add(rs.getString(11));
                v.add(rs.getString(2));
                k.addRow(v);


                int s2 = k.getRowCount();
                jLabel3.setText(s2 + "");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void m() {
        try {
            DefaultTableModel k = (DefaultTableModel) jTable1.getModel();
            k.setRowCount(0);
            ResultSet rs = ovcconn.DB().createStatement().executeQuery("SELECT * FROM `movie_lib` WHERE " + para + " LIKE '" + textFieldCM1.getText() + "%'");
            while (rs.next()) {
                Vector v = new Vector();
               v.add(rs.getString(1));
                v.add(rs.getString(3));
                v.add(rs.getString(5));
                v.add(rs.getString(4));
                v.add(rs.getString(7));
                v.add(rs.getString(11));
                v.add(rs.getString(2));
                k.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
