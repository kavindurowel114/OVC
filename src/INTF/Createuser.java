    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        int i = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this User from Midas Application?", "Delete", JOptionPane.YES_NO_OPTION);
        if (i == JOptionPane.YES_OPTION) {
            try {

                Statement s = ovcconn.DB().createStatement();
                s.executeUpdate("DELETE FROM user WHERE username = '" + textFieldCM1.getText() + "'");
                JOptionPane.showMessageDialog(this, "User Deleted");

                txfna.setText(null);
                txemail.setText(null);
                txln.setText(null);
                txuserna.setText(null);
                txps.setText(null);
                txmn.setText(null);

                JOptionPane.showMessageDialog(this, "User Deleted");

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

