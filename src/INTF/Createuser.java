    private void textFieldCM1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textFieldCM1KeyTyped

        try {
            Statement s = ovcconn.DB().createStatement();
            String ps = new String(txps.getPassword());

            ResultSet rs = s.executeQuery("Select * from user WHERE username='" + textFieldCM1.getText() + "' ");

            //JOptionPane.showMessageDialog(this, "Unkown Username");
            while (rs.next()) {

                if (jRadioButton1.isSelected()) {

                    txfna.setText(rs.getString(3));
                    txln.setText(rs.getString(4));
                    txuserna.setText(rs.getString(1));
                    txps.setText(rs.getString(2));
                    txemail.setText(rs.getString(7));
                    String p = rs.getString(5);

                    if (p.equals("Admin")) {

                        comprivilage.setSelectedItem("Admin");
                       
                        jButton1.setEnabled(false);
                        jButton4.setEnabled(false);
                        jButton3.setEnabled(false);
                    } 
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_textFieldCM1KeyTyped

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {

            Statement s = ovcconn.DB().createStatement();
            String p = new String(txps.getPassword());
            String privilage;
            if (comprivilage.getSelectedIndex() == 1) {
                privilage = "Admin";
            } else {
                privilage = "Standard";
            }

            s.executeUpdate("UPDATE user SET Username = '" + txuserna.getText() + "',password = '" + p + "',first_name = '" + txfna.getText() + "',last_name = '" + txln.getText() + "',mobile = '" + txmn.getText() + "',email = '" + txemail.getText() + "',Privilege_Type = '" + privilage + "' WHERE username = '" + textFieldCM1.getText() + "'");

            txfna.setText(null);
            txemail.setText(null);
            txln.setText(null);
            txuserna.setText(null);
            txps.setText(null);
            txmn.setText(null);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {

            Statement s = ovcconn.DB().createStatement();
            String p = new String(txps.getPassword());

            String privilage;
            String stat;
            if (comprivilage.getSelectedIndex() == 1) {
                privilage = "Admin";
                stat = "null";
            } else {
                privilage = "Standard";
                stat = "null";
            }

            s.executeUpdate("INSERT INTO user VALUES('" + txuserna.getText() + "','" + p + "','" + txfna.getText() + "','" + txln.getText() + "','" + privilage + "','" + txmn.getText() + "','" + txemail.getText() + "')");

            txfna.setText(null);
            txemail.setText(null);
            txln.setText(null);
            txuserna.setText(null);
            txps.setText(null);
            txmn.setText(null);
            JOptionPane.showMessageDialog(this, "User Created Successfully");

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

