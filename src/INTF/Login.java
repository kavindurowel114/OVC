    private void pswKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pswKeyPressed
       
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
        
            try {

            Statement s = ovcconn.DB().createStatement();
            String u = uname.getText();
            String p = new String(psw.getPassword());
            ResultSet rs = s.executeQuery("Select * from user WHERE Username = '" + u + "' and Passsword = '" + p + "'");

            if (rs.next()) {
                
                
                String name = rs.getString("First_Name");
                JOptionPane.showMessageDialog(this, "Welcome  "+name);
                this.dispose();
                new Home().setVisible(true);
               

            } else {

                JOptionPane.showMessageDialog(this, "Username or Password Error!");

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
        
        
        }
               
    }//GEN-LAST:event_pswKeyPressed

